/* ORDER BY */

SELECT
      MENU_CODE,
	  MENU_NAME,
      MENU_PRICE
  FROM TBL_MENU
 -- ORDER BY MENU_PRICE ASC; 생략 가능
 ORDER BY MENU_PRICE;

SELECT
      MENU_CODE,
	  MENU_NAME,
      MENU_PRICE
  FROM TBL_MENU
 ORDER BY MENU_PRICE DESC;

SELECT
       MENU_CODE,
	   MENU_NAME,
       MENU_PRICE
  FROM TBL_MENU
 ORDER BY MENU_PRICE DESC,
         MENU_NAME ASC;
       
SELECT 
       MENU_CODE, 
       MENU_PRICE, 
       MENU_CODE * MENU_PRICE
  FROM
       TBL_MENU
 ORDER BY MENU_CODE * MENU_PRICE DESC;
   
